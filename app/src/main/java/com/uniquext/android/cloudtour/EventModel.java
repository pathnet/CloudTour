public class EventModel {



    private int code;
    private LatLng latLng;

    public EventModel(int code) {
        this(code, null);
    }

    public EventModel(int code, LatLng latLng) {
        this.code = code;
        this.latLng = latLng;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public LatLng getLatLng() {
        return latLng;
    }

    public void setLatLng(LatLng latLng) {
        this.latLng = latLng;
    }
}
